# Profundizando en los sistemas de numeraci√≥n

# Sistema decimal:
a = 10
print(f"a decimal: {a}") # a = 10

# Sistema binario:
a = 0b1010
print(f"a binario: {a}") # a = 10

# Sistema octal
a = 0o12
print(f"a octal: {a}") # a = 10

# Sistema hexadecimal
a = 0xA
print(f"a hexadecimal: {a}") # a = 10

print('------Bases------')
# Base Decimal
a = int('23', 10)
print(f"a = base decimal: {a}")
print('[23 dec = 23 dec]')

# Base Binario
a = int('10111', 2)
print(f"a = base binario: {a}")
print('[10111 bi = 23 dec]')

# Base Octal
a = int('27', 8)
print(f"a = base octal: {a}")
print('[27 oct = 23 dec]')

# Base Hexadecimal
a = int('17', 16)
print(f"a = base hexadecimal: {a}")
print('[17 hex = 23 dec]')

# Base 5, sus valores son de 0 a 4
a = int('43', 5)
print(f"a = base 5: {a}")
print('[43 b5 = 23 dec]')

